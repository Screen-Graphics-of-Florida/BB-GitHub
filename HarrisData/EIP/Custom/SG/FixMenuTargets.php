<?php
// FixMenuTargets.php
// TARGET SCHEMA: SGHDSDATA (EIP Live only)
//
// Fixes FUTRGT='_blank' on the 31 specific SG portal sub-menu entries
// created by SgApplyAll.php (30 rows) and InsertMgmtPortal (1 row).
// Uses an EXPLICIT IN list — does NOT touch any other SYURLM rows.
//
// Deliberately excluded (intentional _blank or no submenus):
//   SGPRICINGINQUIRY  — opens new window by design, never touch
//   SG_DOCUMENTATION  — no submenus, already correct
//   Any other SG* entry not in the list below
//
// Preview: https://portal.screen-graphics.com:5601/Custom/SG/FixMenuTargets.php
// Execute: https://portal.screen-graphics.com:5601/Custom/SG/FixMenuTargets.php?confirm=FIX

$confirm = (isset($_GET['confirm']) && $_GET['confirm'] === 'FIX');

// Exactly the FUIDs generated by SgApplyAll.php Step 2 (5 portals x 6 cats = 30)
// plus SGMGMT_OE from InsertMgmtPortal — nothing else.
$targetFuids = array(
    'SGINQ_ACCT',    'SGINQ_INVMGMT',  'SGINQ_MFG',    'SGINQ_OE',    'SGINQ_PLN',    'SGINQ_PUR',
    'SGDASH_ACCT',   'SGDASH_INVMGMT', 'SGDASH_MFG',   'SGDASH_OE',   'SGDASH_PLN',   'SGDASH_PUR',
    'SGDINT_ACCT',   'SGDINT_INVMGMT', 'SGDINT_MFG',   'SGDINT_OE',   'SGDINT_PLN',   'SGDINT_PUR',
    'SGRPT_ACCT',    'SGRPT_INVMGMT',  'SGRPT_MFG',    'SGRPT_OE',    'SGRPT_PLN',    'SGRPT_PUR',
    'SGSOP_ACCT',    'SGSOP_INVMGMT',  'SGSOP_MFG',    'SGSOP_OE',    'SGSOP_PLN',    'SGSOP_PUR',
    'SGMGMT_OE',
);

$inList = "'" . implode("','", $targetFuids) . "'";

$conn = db2_connect('*LOCAL', '', '');
if (!$conn) die('DB error: ' . htmlspecialchars(db2_conn_errormsg()));

$previewSql = "SELECT FUID, FUTRGT, FUTITL FROM SGHDSDATA.SYURLM
               WHERE FUID IN ($inList) AND RTRIM(FUTRGT) = '_blank'
               ORDER BY FUID";

$updateSql  = "UPDATE SGHDSDATA.SYURLM SET FUTRGT = ''
               WHERE FUID IN ($inList) AND RTRIM(FUTRGT) = '_blank'";

$rows = array();
$s = @db2_exec($conn, $previewSql);
if ($s) { while ($r = db2_fetch_assoc($s)) $rows[] = $r; }

$updateResult = null;
$rowsAffected = 0;
if ($confirm && !empty($rows)) {
    $updateResult = @db2_exec($conn, $updateSql);
    if ($updateResult !== false) {
        $rowsAffected = db2_num_rows($updateResult);
    }
}

db2_close($conn);
?>
