<?php if ($confirm): ?>
  <?php if ($updateResult === false): ?>
    <div class="warn"><strong>UPDATE failed.</strong> <?php echo htmlspecialchars(db2_stmt_errormsg()); ?></div>
  <?php elseif ($rowsAffected > 0): ?>
    <div class="ok"><strong>Done.</strong> <?php echo $rowsAffected; ?> row(s) updated. EIP submenu items will now open in the same window. Log out and back in to verify.</div>
  <?php else: ?>
    <div class="ok"><strong>Nothing to do.</strong> No target rows have FUTRGT='_blank' &mdash; already fixed or never set.</div>
  <?php endif; ?>
<?php else: ?>
  <?php if (empty($rows)): ?>
    <div class="ok"><strong>Nothing to fix.</strong> None of the 31 target FUIDs in SGHDSDATA.SYURLM have FUTRGT='_blank'. No changes needed.</div>
  <?php else: ?>
    <div class="warn">
      <strong><?php echo count($rows); ?> of the 31 target row(s) have FUTRGT='_blank' and will be updated.</strong><br>
      This makes these EIP submenu flyout items open in the same window instead of a new window.
      SGPRICINGINQUIRY and all other non-listed entries are untouched.
    </div>
    <table>
      <tr><th>FUID</th><th>Current FUTRGT</th><th>Title</th></tr>
      <?php foreach ($rows as $r): ?>
      <tr>
        <td><?php echo htmlspecialchars($r['FUID']); ?></td>
        <td style="color:#c62828"><?php echo htmlspecialchars($r['FUTRGT']); ?></td>
